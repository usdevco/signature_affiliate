</ul>
<p class="p8">&nbsp;</p>
<ul class="ul1">
<li class="li6"><strong>Financing Company&rsquo;s Failure to Pay</strong>.<span class="Apple-converted-space">&nbsp; </span>If, for any reason whatsoever, the Financing Company fails to pay the total amount due Contractor under the Contract within 30 days of Contractor&rsquo;s completion of the Work, including, but not limited to, (i) Client&rsquo;s failure to provide necessary documentation to the Financing Company, (ii) Client&rsquo;s failure to perform the additional work required for the approval of final inspection (if applicable), (iii) pre-existing conditions, objections or complaints about items other than the Work, (iv) defects/damage in the Materials (as defined in the Contract) that are the product of the fabrication or shipment of the Materials by the manufacturer, (v) any issue directly attributable to the Client or the product manufacturer or (vi) any issue which is otherwise outside of Contractor&rsquo;s control, then Client shall be responsible for the immediate payment of all amounts due Contractor.</li>
</ul>
